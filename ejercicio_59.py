# ÁREA Y PERÍMETRO DE UN ROMBO

print(f"ÁREA DEL ROMBO")
print(f"DIAMETRO MAYOR : ", end = "")
D = float(input())
print(f"DIAMETRO MENOR : ", end = "")
dd = float(input())

A =  (D * dd) / 2

print(f"ÁREA : {A} cm.")

print(f"")
print(f"PERÍMETRO DEL ROMBO")
print(f"LADO : ", end = "")
l = float(input())

P = 4 * l

print(f"PERÍMETRO : {P} cm.")