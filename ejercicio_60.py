# ÁREA Y PERÍMETRO DE UN PENTÁGONO

print(f"ÁREA DEL PENTÁGONO")
print(f"PERÍMETRO : ", end = "")
P = float(input())
print(f"APOTEMA : ", end = "")
ap = float(input())

A =  (P * ap) / 2

print(f"ÁREA : {A} cm.")

print(f"")
print(f"PERÍMETRO DEL PENTÁGONO")
print(f"LADO : ", end = "")
l = float(input())

P = 5 * l

print(f"PERÍMETRO : {P} cm.")