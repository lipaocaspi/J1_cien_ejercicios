# ÁREA Y PERÍMETRO DE UN PENTÁGONO

print("ÁREA DEL PENTÁGONO")
print("PERÍMETRO : ", end = "")
P = float(input())
print("APOTEMA : ", end = "")
ap = float(input())
A =  (P * ap) / 2
print("ÁREA : ", A, "cm.")
print("")
print("PERÍMETRO DEL PENTÁGONO")
print("LADO : ", end = "")
l = float(input())
