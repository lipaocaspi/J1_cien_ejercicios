# ÁREA Y PERÍMETRO DE UN HEXÁGONO

print(f"PERÍMETRO : ", end = "")
P = float(input())
print(f"APOTEMA : ", end = "")
ap = float(input())

A = (P * ap) / 2

print(f"ÁREA : {A} cm.")

print(f"")
print(f"PERÍMETRO DEL HEXÁGONO")
print(f"LADO : ", end = "")
l = float(input())

P = 6 * l

print(f"PERIMETRO : {P} cm.")