# ÁREA Y PERÍMETRO DE UN PARALELOGRAMO

print(f"ÁREA DEL PARALELOGRAMO")
print(f"BASE : ", end = "")
b = float(input())
print(f"ALTURA : ", end = "")
h = float(input())

A =  b * h

print(f"ÁREA : {A} cm.")

print(f"")
print(f"PERÍMETRO DEL PARALELOGRAMO")

P = (2 * h) + (2 * b)

print(f"PERÍMETRO : {P} cm.")