# ÁREA Y PERÍMETRO DE UN TRIÁNGULO

print(f"ÁREA DEL TRIÁNGULO")
print(f"BASE : ", end = "")
b = float(input())
print(f"ALTURA : ", end = "")
h = float(input())

A = (b * h) / 2

print(f"ÁREA : {A} cm.")

print(f"")
print(f"PERÍMETRO DEL TRIÁNGULO EQUILÁTERO")
print(f"LADO : ", end = "")
L = float(input())

P = L * 3

print(f"PERIMETRO : {P} cm.")