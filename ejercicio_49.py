# ÁREA Y PERÍMETRO DE UN TRIÁNGULO

print("ÁREA DEL TRIÁNGULO")
print("BASE : ", end = "")
b = float(input())
print("ALTURA : ", end = "")
h = float(input())
A = (b * h) / 2
print("ÁREA : ", A, " cm.")
print("")
print("PERÍMETRO DEL TRIÁNGULO EQUILÁTERO")
print("LADO : ", end = "")
L = float(input())
P = L * 3
print("PERIMETRO : ", P, " cm.")